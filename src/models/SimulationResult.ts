/**
 * Модель результата симуляции роста растений
 * 
 * Этот интерфейс описывает структуру данных, возвращаемых экспертной системой
 * после анализа параметров и расчета прогноза роста растений.
 */

/**
 * Результат симуляции роста растений в гидропонной системе
 * 
 * Содержит все ключевые показатели, рассчитанные экспертной системой:
 * 
 * @param isViable - Жизнеспособность растения (true - будет расти, false - погибнет)
 * @param yieldPercentage - Ожидаемая урожайность в процентах от максимальной (0-100)
 * @param expectedGrams - Ожидаемый урожай в граммах (опционально)
 * @param yieldPerSquareMeter - Урожайность в кг/м² (опционально)
 * @param growthTime - Время до сбора урожая в днях
 * @param issues - Список выявленных проблем с параметрами
 * @param recommendations - Рекомендации по улучшению условий выращивания
 */
export interface SimulationResult {
    /** Жизнеспособность растения - основной показатель успешности выращивания */
    isViable: boolean;
    
    /** Урожайность в процентах от максимально возможной для данной культуры */
    yieldPercentage: number; // 0..100
    
    /** Ожидаемый урожай в граммах (рассчитывается на основе yieldPercentage и maxYield культуры) */
    expectedGrams?: number; // ожидаемые граммы
    
    /** Урожайность в килограммах на квадратный метр (для сравнения с традиционным земледелием) */
    yieldPerSquareMeter?: number; // урожайность в кг/м²
    
    /** Время до сбора урожая в днях (может увеличиваться при неоптимальных условиях) */
    growthTime: number;
    
    /** Список проблем, выявленных при анализе параметров */
    issues: string[];
    
    /** Рекомендации по корректировке параметров для улучшения результата */
    recommendations: string[];
}