/**
 * Константы параметров культур для гидропонной системы
 * 
 * Этот файл импортирует базу знаний о культурах из JSON файла
 * и предоставляет типизированный доступ к параметрам выращивания.
 * 
 * База знаний содержит:
 * - Оптимальные диапазоны параметров для каждой культуры
 * - Критические границы (за пределами которых растение погибнет)
 * - Временные параметры роста
 * - Максимальные урожаи
 */

import crops from '../data/crops.json';

/**
 * База знаний о параметрах выращивания культур
 * 
 * Содержит конфигурации для всех поддерживаемых культур
 * с их оптимальными параметрами и характеристиками роста.
 */
export const CROP_PARAMETERS = crops as Record<string, any>;

/**
 * Тип культур для типобезопасного доступа
 * 
 * Автоматически извлекается из ключей CROP_PARAMETERS,
 * что обеспечивает синхронизацию типов с данными.
 */
export type CropType = keyof typeof CROP_PARAMETERS;